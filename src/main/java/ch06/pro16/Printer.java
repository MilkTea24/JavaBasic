package ch06.pro16;

public class Printer {
    void println(int number){
        System.out.println(number);
    }
    void println(boolean bool){
        System.out.println(bool);
    }
    void println(double number){
        System.out.println(number);
    }
    void println(String name){
        System.out.println(name);
    }
}
